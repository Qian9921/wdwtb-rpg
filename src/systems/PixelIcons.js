// 便捷助手:创建一个像素图标 image(替代 emoji 文本)。scene 须先 ensurePixelIcons。
// @param scene, x, y, iconKey(ICON_KEYS.xxx), tint 颜色, size 目标像素尺寸(默认24)
export function makeIcon(scene, x, y, iconKey, tint = 0xffffff, size = 24) {
  const img = scene.add.image(x, y, iconKey).setTint(tint);
  if (size !== 24) img.setDisplaySize(size, size);
  return img;
}

// 在场景中确保所有图标纹理已生成(幂等)
export function ensurePixelIcons(scene) {
  for (const [name, rows] of Object.entries(GLYPHS)) {
    const key = ICON_KEYS[name];
    if (scene.textures.exists(key)) continue;
    const g = scene.make.graphics({ add: false });
    g.fillStyle(0xffffff, 1);
    for (let y = 0; y < GRID; y++) {
      const row = rows[y] || '';
      for (let x = 0; x < GRID; x++) {
        if (row[x] === '1') g.fillRect(x * PX, y * PX, PX, PX);
      }
    }
    g.generateTexture(key, GRID * PX, GRID * PX);
    g.destroy();
  }
}
